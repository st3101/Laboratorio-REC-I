
int suma(int n1, int n2)
{
    float resultado=n1+n2;
    return resultado;
}
int resta(int n1,int n2)
{
    float resultado=(float)n1-n2;
    return resultado;
}
int multiplicion(int n1,int n2)
{
    float resultado=(float)n1*n2;
    return resultado;
}
float division(int n1,int n2)
{
float resultado=(float)n1/n2;
return resultado;
}
